#include "funcs.h"
double MySum(double a, double b)
{
    return a + b;
}
double MySub(double a, double b)
{
    return a - b;
}
double MyMul(double a, double b)
{
    return a * b;
}
double MyDiv(double a, double b)
{
    return a / b;
}
double MyPow(double a, double b)
{
    return exp(b * log(a));
}
